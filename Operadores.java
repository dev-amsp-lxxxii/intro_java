
public class Operadores {

    public static void main(String[] args) {
        // Operadores Aritméticos
        int aOpAri = 10;
        int bOpAri = 2;

        System.out.println(aOpAri + bOpAri);
        System.out.println(aOpAri - bOpAri);
        System.out.println(aOpAri * bOpAri);
        System.out.println(aOpAri / bOpAri);
        System.out.println(aOpAri % bOpAri);

        // Operadores de Compraração
        int aOpComp = 10;
        int bOpComp = 20;

        System.out.println(aOpComp == bOpComp);
        System.out.println(aOpComp != bOpComp);
        System.out.println(aOpComp > bOpComp);
        System.out.println(aOpComp < bOpComp);
        System.out.println(aOpComp >= bOpComp);
        System.out.println(aOpComp <= bOpComp);
    }

}
