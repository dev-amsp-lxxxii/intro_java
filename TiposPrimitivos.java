public class TiposPrimitivos {
    public static void main(String[] args) {
        byte idade = 25;
        short ano = 2025;
        int populacaoCidade = 500000;
        long populacaoMundial = 7800000000L;
        float altura = 1.75f;
        double salario = 4500.50;
        boolean estudante = true;
        char inicialNome = 'A';

        System.out.println("Idade: " + idade);
        System.out.println("Ano: " + ano);
        System.out.println("População Cidade: " + populacaoCidade);
        System.out.println("População Mundial: " + populacaoMundial);
        System.out.println("Altura" + altura);
        System.out.println("Salário: " + salario);
        System.out.println("Estudante: " + estudante);
        System.out.println("Inicial Nome: " + inicialNome);
    }
}
