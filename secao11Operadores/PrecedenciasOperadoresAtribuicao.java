
public class PrecedenciasOperadoresAtribuicao {

    public static void main(String[] args) {
        int numero = 5;

        ++numero;
        System.out.println(numero);

        numero++;
        System.out.println(numero);

        --numero;
        System.out.println(numero);

        numero--;
        System.out.println(numero);
    }
}
